export var PeriodUnit;
(function (PeriodUnit) {
    PeriodUnit[PeriodUnit["SECONDS"] = 1] = "SECONDS";
    PeriodUnit[PeriodUnit["MINUTES"] = 60] = "MINUTES";
    PeriodUnit[PeriodUnit["HOURS"] = 3600] = "HOURS";
    PeriodUnit[PeriodUnit["DAYS"] = 86400] = "DAYS";
})(PeriodUnit || (PeriodUnit = {}));
